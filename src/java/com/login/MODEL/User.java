package com.login.MODEL;

/**
 *
 * @author SAAD
 */
public class User {

    /**
     * @return the user
     */
    public String getUser() {
        return user;
    }

    public User() {
    }

    /**
     * @param user the user to set
     */
    public void setUser(String user) {
        this.user = user;
    }

    /**
     * @return the pass
     */
    public String getPass() {
        return pass;
    }

    /**
     * @param pass the pass to set
     */
    public void setPass(String pass) {
        this.pass = pass;
    }

    /**
     * @return the role
     */
    public String getRole() {
        return role;
    }

    /**
     * @param role the role to set
     */
    public void setRole(String role) {
        this.role = role;
    }

    public User(int id, String user,String role) {
        this.id = id;
        this.user = user;
      
        this.role = role;
    }

 
    private int id;
    private String user;
    private String pass;
    private String role;

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }
    
}
